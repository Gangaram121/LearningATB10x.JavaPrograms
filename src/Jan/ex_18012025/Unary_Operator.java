package Jan.ex_18012025;

public class Unary_Operator {
    public static void main(String[] args) {
        // Unary Operator
        int a = +10;
        int a1 = -110;
        System.out.println(a);
        System.out.println(a1);

        int b = 6;
        b = b+4;
        System.out.println(b);

    }
}
