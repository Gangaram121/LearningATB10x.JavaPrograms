public class Max_number_T_O {
    public static void main(String[] args) {
        // Find the maximum number between two numbers.
        // X and Y -> Max X, y -> Ternary Op.
        int x = 40;
        int y  = 20;
        String max = x > y ? "X is max" : "Y is max";
        System.out.println(max);
    }
}
