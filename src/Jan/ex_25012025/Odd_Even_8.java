public class Odd_Even_8 {
    public static void main(String[] args) {
//        Ram - to find even number 1to 50
        for (int i = 0; i <= 50; i++){
            if (i % 2 == 0) {
                System.out.println("even ->" +i);
            }
        else{
            System.out.println("odd ->" +i);
        }
        }
    }
}
